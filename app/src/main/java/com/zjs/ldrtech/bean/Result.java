public class Result implements Serializable {
    private String who;
    private String publiishedAt;
    private String desc;
    private String type;
    private String url;
    private boolean used;
    private String objectId;
    private String createdAt;
    private String updatedAt;
    private DateTime createdDateTime;

    public String getWho() {
        return who;
    }

    public void setWho(String who) {
        this.who = who;
    }

    public String getPubliishedAt() {
        return publiishedAt;
    }

    public void setPubliishedAt(String publiishedAt) {
        this.publiishedAt = publiishedAt;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public boolean getUsed() {
        return used;
    }

    public void setUsed(boolean used) {
        this.used = used;
    }

    public String getObjectId() {
        return objectId;
    }

    public void setObjectId(String objectId) {
        this.objectId = objectId;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public String getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(String updatedAt) {
        this.updatedAt = updatedAt;
    }
    public DateTime getCreatedDateTime() {
        if (createdDateTime==null) {
            createdDateTime = DateUtils.formatDateFromStr(createdAt);

        }
        return createdDateTime;

    }

    @Override
    public String toString() {
        return "who" + who + "desc" + desc + "url" + url;
    }
}
