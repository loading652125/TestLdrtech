public abstract class BaseFragment extends RxFragment {
    protected View mContaninView;

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setHasOptionsMenu(true);
    }

    @Nullable
    @Override
    public View onCreateView(LayoutInflater inflater, @Nullable ViewGroup container, @Nullable Bundle savedInstanceState) {
        if (mContaninView == null) {
            mContaninView = inflater.inflate(returnLayoutID(),container,false);
        }
        return mContaninView;
    }

    @Override
    public void onViewCreated(View view, Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);
        ButterKnife.bind(this, view);
        afterCreate(savedInstanceState);
    }

    protected abstract void afterCreate(Bundle savedInstanceState);

    protected abstract int returnLayoutID();

    public void showSnackbar(int stringRsId) {
        Snackbar.make(mContaninView,stringRsId,Snackbar.LENGTH_SHORT).show();
    }
    public void showSnackbar(String string) {
        Snackbar.make(mContaninView, string,Snackbar.LENGTH_SHORT).show();
    }
}
