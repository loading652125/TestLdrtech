import android.os.Bundle;
import android.support.annotation.Nullable;
import android.support.v4.app.Fragment;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.FrameLayout;
import android.widget.ImageView;
import com.bumptech.glide.Glide;

public class SlideImageUtil extends Fragment {
    private static final String SKIP_IMAGE = "skipId";
    private int imageId;

    public static SlideImageUtil newInstance(int imageId) {
        SlideImageUtil sampleSlide = new SlideImageUtil();

        Bundle args = new Bundle();
        args.putInt(SKIP_IMAGE, imageId);
        sampleSlide.setArguments(args);
        return sampleSlide;
    }

    @Override
    public void onCreate(@Nullable Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);

        if (getArguments() != null && getArguments().containsKey(SKIP_IMAGE)) {
            imageId = getArguments().getInt(SKIP_IMAGE);
        }
    }

    @Nullable
    @Override
    public View onCreateView(LayoutInflater inflater, @Nullable ViewGroup container,
                             @Nullable Bundle savedInstanceState) {
        ImageView image = new ImageView(getContext());
        Glide.with(this).load(imageId).asBitmap().into(image);
        image.setScaleType(ImageView.ScaleType.FIT_XY);
        image.setLayoutParams(new FrameLayout.LayoutParams(ViewGroup.LayoutParams.MATCH_PARENT,
                ViewGroup.LayoutParams.MATCH_PARENT));
        return image;
    }

}
